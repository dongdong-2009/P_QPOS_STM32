void vLCD_ShowTime(void)
{
	unsigned int year;
	unsigned char i,j;
	unsigned char time[]="2000/00/00   00:00:00";
	
	if(RTC_Get())
	{
		vLCD_GUI_ShowMid12X12("请检查纽扣电池",4);
		return;
	}
	else
	{}
	
	i=(g_sCalendar.w_date/10)+0x30;// 日
	j=(g_sCalendar.w_date%10)+0x30;

	time[8]=i;
	time[9]=j;
	
	i=(g_sCalendar.w_month/10)+0x30;// 月
	j=(g_sCalendar.w_month%10)+0x30;

	time[5]=i;
	time[6]=j;
	
	year=g_sCalendar.w_year-2000;
	i=(year/10)+0x30;// 年
	j=(year%10)+0x30;

	time[2]=i;
	time[3]=j;
	
	i=(g_sCalendar.min/10)+0x30;//分
	j=(g_sCalendar.min%10)+0x30;
	time[16]=i;
	time[17]=j;
	i=(g_sCalendar.hour/10)+0x30;//时
	j=(g_sCalendar.hour%10)+0x30;
	time[13]=i;
	time[14]=j;

	i=(g_sCalendar.sec/10)+0x30;//秒
	j=(g_sCalendar.sec%10)+0x30;
	time[19]=i;
	time[20]=j;

	vLCD_GUI_ShowMid12X12(time,4);
}

